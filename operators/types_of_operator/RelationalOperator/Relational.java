class Relational {
    public static void main(String[] args) {
        {
            int a = 14;
            int b = 6;
            System.out.println(a < b);
            System.out.println(a > b);
            System.out.println(a >= b);
            // b=14;
            System.out.println(a > b);
            System.out.println(a == b);
            System.out.println(a != b);

        }
    }
}
