public class D {

    static int i;
    static double d;
    static char k;
    static boolean b;
    static String s;

    public static void main(String[] args) {
        System.out.println(i); // default values 0
        System.out.println(d); // default values 0.0
        System.out.println(k); // default values empty
        System.out.println(b); // default values false
        System.out.println(s); // default values null
    }
}
